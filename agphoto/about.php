        <h2 class="module-heading text-center" id="faq">FAQ</h2>
        <div class="about-faq column small-10 small-offset-1">
            <p><span>How many weddings do you photograph per year?</span>I photograph a maximum of 20 weddings per year. For each wedding, I really try to capture not just the story, but the less obvious moments too. I think that keeping that number under 20 gives me the time to connect with each couple, so that I understand what makes their relationship special, and start to really see those quiet moments.</p>
            <p><span>How many edited files will we get from our wedding?</span>Every couple that I work with gets a flash drive of fully edited images with personal printing rights, but the total number of files really depends on your day. For most weddings, I deliver between 300-500 fully-polished images.</p>
            <p><span>Can you recommend other vendors you like working with?</span>Yes! Choosing vendors can be a little bit scary, but we have a select few that we confidently recommend. Take a look at our preferred florists, makeup artists, DJs, and videographers on <a href="/vendors" title="Amy Galbraith Photography's list of recommended vendors">our preferred vendor page</a>.</p>
            <p><span>How did you get into photography?</span>I first began shooting professionally in 2009 as an action sports photographer. Back then, I focused on adventure sports -- snowboarding, kayaking, mountain biking, and so on. A neighbor that knew I was a photographer asked if I would shoot her wedding, and I nervously agreed. That one wedding opened the door to a world I never knew existed! The authenticity of the emotions, what happens when careful planning collides with the unpredictable -- it spoke to me, and I have never looked back. I'm lucky to say that I've found my passion and I love what I do!</p>
            <p><span>Do you travel for weddings?</span>Yes! We have photographed weddings all across Washington, Wyoming, Oregon and Idaho and absolutely love to travel. All weddings within 50 miles of Seattle or Jackson Hole do not incur a travel fee but please inquire about booking a wedding out-of-state or across the world. If you love our work, we are happy to work with you to make it happen!</p>
            <p><span>What's involved in booking you for our wedding?</span>Before we talk about booking, we really should just talk! Whether it's over coffee or a cocktail or a chat on the phone, I really want to have a normal conversation in a relaxed setting first. We'll tell you a little bit more about our philosophy and how we work, and you can ask us any questions you have. We can talk logistics after you know you would like to move forward! The best way to get in contact is through <a href="/contact" title="Amy Galbraith Photography Contact Form link">my contact form</a>, and you can check out pricing <a href="/investment" title="Amy Galbraith Photography Investment page link">here</a>.</p>
            <p><span>How do we book you for an engagement or portrait session?</span>I take a pretty similar approach here as I do with weddings -- I always like to have some time to just get to know you a little bit first! Get in touch through the <a href="/contact" title="Amy Galbraith Photography Contact Form link">contact form</a>, and we can set up some time to talk. Check out pricing <a href="/investment" title="Amy Galbraith Photography Investment page link">here</a>.</p>
            <p><span>How would you describe you style?</span>We think the photos that resonate most with anyone are always the ones that tell two stories. We want each image to play it's part in describing the story of your day, just as it happened. And we want each image to capture real, candid emotion -- the kind that sticks so strongly in your mind that it has the power to transport you back to that exact moment on that day. We avoid coaching, catching real emotion in natural moments.</p>
